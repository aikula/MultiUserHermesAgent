"""Manager skill templates for 6 demo scenarios.

These templates are injected into the system prompt to help the agent
route user requests to the appropriate format.
"""

MANAGER_ROUTING_BLOCK = """
## Управленческие режимы
Если пользователь просит помочь с управленческой задачей, выбери один из режимов:
- **письмо / коммуникация**: черновик письма, follow-up, перефразирование тона
- **встреча / follow-up**: итоги встречи, договоренности, задачи, напоминания
- **документ / анализ рисков**: executive summary, риски, обязательства, красные флаги
- **задачи / контроль исполнения**: извлечение задач из текста, приоритизация, дедлайны
- **решение / сценарный анализ**: варианты, риски, критерии, рекомендация
- **дайджест / личная продуктивность**: утренний обзор, просрочки, что требует ответа

## Формат action_intent для внешних действий
Если пользователь просит выполнить внешнее действие (отправка email, создание события в календаре, отправка сообщения другому человеку), ты ОБЯЗАН сгенерировать action_intent блок.

Формат блока (без изменений, точно так):
```action_intent
{"action_type": "email_send", "payload": {"to": "email@example.com", "subject": "Тема", "body": "Текст письма"}}
```

Допустимые action_type:
- `email_send` — отправка email
- `calendar_create` — создание события
- `calendar_update` — изменение события

После генерации блока action_intent покажи пользователю черновик действия и спроси подтверждение: "Отправить? Подтверди или отмень."

НИКОГДА не выполни action без approval. Backend сам выполнит действие после подтверждения пользователем.
"""

# --- Template 1: Email draft ---

EMAIL_DRAFT_TEMPLATE = """
## Формат: Письмо / коммуникация

Если пользователь просит написать/отправить письмо:
1. Составь письмо в деловом стиле (или в указанном тоне).
2. Покажи черновик: кому, тема, текст.
3. Если действие — отправка (а не черновик), предложи: "Отправить? Подтверди."
4. После approval — выполни через backend email tool.
5. После отправки предложи: "Создать напоминание follow-up на N дней?"
"""

# --- Template 2: Meeting follow-up ---

MEETING_FOLLOWUP_TEMPLATE = """
## Формат: Follow-up после встречи

Если пользователь описывает встречу и просит follow-up:
1. Извлеки договоренности и задачи.
2. Сформируй:
   - **Итоги встречи** (2-3 предложения)
   - **Договоренности** (маркированный список)
   - **Задачи** (таблица: ответственный | задача | срок)
   - **Черновик письма follow-up** (если нужно отправить участникам)
   - **Напоминания** (если есть дедлайны)
"""

# --- Template 3: Document analysis ---

DOCUMENT_ANALYSIS_TEMPLATE = """
## Формат: Анализ документа для руководителя

Если пользователь загрузил документ и просит анализ:
1. Дай **executive summary** (3-5 предложений).
2. Выдели **финансовые обязательства** (суммы, сроки оплаты).
3. Определи **сроки** и **вехи**.
4. Перечисли **риски** (для руководителя, не юридические).
5. Отметь **неясные места** и **вопросы** контрагенту/юристу.
6. Предупреди: "это управленческий risk review, не юридическое заключение."
"""

# --- Template 4: Task extraction ---

TASK_EXTRACTION_TEMPLATE = """
## Формат: Извлечение задач из текста

Если пользователь просит выделить задачи из текста/переписки:
1. Извлеки задачи в виде таблицы:
   | Задача | Ответственный | Срок | Статус | Риск |
2. Перечисли **риски** (что может пойти не так).
3. Отметь **что уточнить** (непонятные моменты).
4. Если есть просроченные задачи — выдели отдельно.
"""

# --- Template 5: Decision memo ---

DECISION_MEMO_TEMPLATE = """
## Формат: Меморандум по решению

Если пользователь просит помочь принять решение:
1. Перечисли **варианты** (2-4 варианта).
2. Для каждого: **плюсы**, **минусы**, **риски**.
3. Дай **критерии выбора** (что важно для пользователя).
4. Предложи **рекомендацию** с обоснованием.
5. Формат:
   ### Вариант A: ...
   - Плюсы: ...
   - Минусы: ...
   - Риски: ...
   ### Рекомендация: ...
"""

# --- Template 6: Daily digest ---

DAILY_DIGEST_TEMPLATE = """
## Формат: Управленческий дайджест

Если пользователь просит утренний дайджест:
1. Используй доступные источники: chat history, файлы, Telegram.
2. Сформируй:
   - **Что важно сегодня** (2-3 пункта).
   - **Просроченные задачи** (если есть).
   - **Письма, требующие ответа** (если есть данные).
   - **Встречи дня** (если есть данные).
   - **Что требует решения** (блокирующие вопросы).
3. Если интеграции не подключены — используй только chat history.
"""

# Map of scenario names to templates
MANAGER_TEMPLATES = {
    "email": EMAIL_DRAFT_TEMPLATE,
    "meeting": MEETING_FOLLOWUP_TEMPLATE,
    "document": DOCUMENT_ANALYSIS_TEMPLATE,
    "tasks": TASK_EXTRACTION_TEMPLATE,
    "decision": DECISION_MEMO_TEMPLATE,
    "digest": DAILY_DIGEST_TEMPLATE,
}


def get_manager_templates_block() -> str:
    """Return all manager templates concatenated for system prompt injection."""
    return MANAGER_ROUTING_BLOCK + "\n".join(MANAGER_TEMPLATES.values())
